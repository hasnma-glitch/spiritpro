#include<stdio.h>
#include<stdlib.h>

main()
{
	int troco, quantidade, total, produtos, arroz, feijao, acucar; 
	
	printf("Digite a quantidade do arroz: ");
	scanf("%f", &arroz);
	printf("Digite a quantidade do feijao: ");
	scanf("%f", &feijao);
	printf("Digite a quantidade do acucar: ");
	scanf("%f", &acucar);
	produtos=arroz+feijao+acucar+total;
	quantidade*produtos;
	printf("\nProdutos: %f\n", arroz, feijao, acucar, total, troco);
	scanf("Troco: %f\n", total, troco);
	system("pause");
}
